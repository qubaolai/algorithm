class Solution {
    public int searchInsert(int[] nums, int target) {
        if (nums[0] > target) {
            return 0;
        }
        if (nums[nums.length - 1] < target) {
            return nums.length;
        }
        // å®šä¹‰å·¦æŒ‡é’ˆ
        int left = 0;
        // å®šä¹‰å³æŒ‡é’ˆ
        int right = nums.length - 1;
        // å¾ªç¯
        while (left <= right) {
            int mid = left + ((right - left) >> 1);
            if (nums[mid] == target) {
                return mid;
            }
            if (nums[mid] < target) {
                left = mid + 1;
            } else {
                right = mid - 1;
            }
        }
        return left;
    }
}
